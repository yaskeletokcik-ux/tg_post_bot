@dataclass
class TemplateMatch:
    """Класс для хранения информации о найденном шаблоне"""
    template_index: int
    matched_text: str


def check_message_against_templates(text: str) -> bool:
    """
    Проверяет сообщение на соответствие шаблонам

    Args:
        text: Текст сообщения

    Returns:
        True если сообщение соответствует одному из шаблонов
    """
    if not text:
        return False

    # Очищаем текст от лишних пробелов для лучшего сравнения
    cleaned_text = ' '.join(text.split())

    for i, pattern in enumerate(COMPILED_TEMPLATES):
        if pattern.search(cleaned_text):
            print(f"✅ Найден шаблон {i + 1}")
            return True

    return False


def should_ignore_message(message: Message) -> bool:
    """
    Проверяет, нужно ли игнорировать сообщение

    Args:
        message: Объект сообщения

    Returns:
        True если сообщение нужно игнорировать
    """
    # Игнорируем сообщения от ботов
    if message.from_user and message.from_user.is_bot:
        # Проверяем, не входит ли бот в список игнорируемых
        if message.from_user.id in IGNORED_BOT_IDS:
            print(f"🤖 Игнорируем сообщение от бота {message.from_user.username or message.from_user.id}")
            return True

    # Игнорируем команды (начинаются с /)
    if message.text and message.text.startswith('/'):
        print("⚠️ Игнорируем команду")
        return True

    return False


